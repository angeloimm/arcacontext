package it.olegna.arca.context.exception;

public class ArcaContextDataReaderException extends Exception {

	private static final long serialVersionUID = -8445028957099703191L;

	public ArcaContextDataReaderException() {
		super();
	}

	public ArcaContextDataReaderException(String message, Throwable cause) {
		super(message, cause);
	}

	public ArcaContextDataReaderException(String message) {
		super(message);
	}

	public ArcaContextDataReaderException(Throwable cause) {
		super(cause);
	}
}
